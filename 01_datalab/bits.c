/*
 * fitsBits - return 1 if x can be represented as an
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  int x_msb_mask = x >> 31;
  return !(((~x & x_msb_mask) + (x & ~x_msb_mask)) >> (n + ~1 + 1));
}
/*
 * rempwr2 - Compute x%(2^n), for 0 <= n <= 30
 *   Negative arguments should yield negative remainders
 *   Examples: rempwr2(15,2) = 3, rempwr2(-35,3) = -3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int rempwr2(int x, int n) {
  int x_msb = (unsigned int) x >> 31;
  int x_msb_mask = x >> 31;
  int remainders = (((~x & x_msb_mask) + (x & ~x_msb_mask) + x_msb) & ~((int)0x80000000 >> (32 + ~n)));
  return (x_msb_mask & ~remainders) + (~x_msb_mask & remainders) + x_msb;
}
/*
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1,
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
  int sum = x + y;
  return  !( ( ( x ^ sum ) & ( y ^ sum ) )  >> 31 );
}
